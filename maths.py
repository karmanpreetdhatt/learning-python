from math import sqrt

def addition(num1, num2):
    result = num1+num2
    return result

def subtraction(num1, num2):
    result = num1-num2
    return result

def multiplication(num1, num2):
    result = num1*num2
    return result

def division(num1, num2):
    result = num1/num2
    return result

def square(num):
    result = num * num
    return result

def square_root(num):
    result = sqrt(num)
    return result    